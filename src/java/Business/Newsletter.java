package Business;

import java.util.Objects;

/**
 *
 * @author Ahmed Khan
 */
public class Newsletter {
    
    private int id;
    private String email;

    public Newsletter() {
    }

    public Newsletter(int id, String email) {
        this.id = id;
        this.email = email;
    }
    public Newsletter(String emailName) {
        this.email = emailName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 29 * hash + this.id;
        hash = 29 * hash + Objects.hashCode(this.email);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Newsletter other = (Newsletter) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.email, other.email)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Newsletter{" + "id=" + id + ", email=" + email + '}';
    }
    
    
    
}
